package Modelo;

/**
 *
 * @author jc997
 */
public class Tareas {
    
    private int id;
    private String titulo;
    private String descripcion;
    private String  hora;

    public Tareas(int id, String titulo, String descripcion, String hora) {
        this.id = id;
        this.titulo = titulo;
        this.descripcion = descripcion;
        this.hora = hora;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public String getHora() {
        return hora;
    }

    public void setHora(String hora) {
        this.hora = hora;
    }
    
    
    
            
    
}
